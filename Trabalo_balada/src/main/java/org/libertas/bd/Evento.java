package org.libertas.bd;

public class Evento {
 private int idevento;
 private String tema;
 private double valor_entrada;
 private String data_evento;
 private String local_evento;
private String horario;
public int getIdevento() {
	return idevento;
}
public void setIdevento(int idevento) {
	this.idevento = idevento;
}
public String getTema() {
	return tema;
}
public void setTema(String tema) {
	this.tema = tema;
}
public double getValor_entrada() {
	return valor_entrada;
}
public void setValor_entrada(double valor_entrada) {
	this.valor_entrada = valor_entrada;
}
public String getData_evento() {
	return data_evento;
}
public void setData_evento(String data_evento) {
	this.data_evento = data_evento;
}
public String getLocal_evento() {
	return local_evento;
}
public void setLocal_evento(String local_evento) {
	this.local_evento = local_evento;
}
public String getHorario() {
	return horario;
}
public void setHorario(String horario) {
	this.horario = horario;
}

}
 

